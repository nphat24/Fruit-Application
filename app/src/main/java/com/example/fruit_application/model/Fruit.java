package com.example.fruit_application.model;

public class Fruit {
    int id;
    int img;
    String name;
    String price;

    public Fruit(int id, int img, String name, String price) {
        this.id = id;
        this.img = img;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
